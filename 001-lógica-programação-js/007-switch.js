let dia = ""
let diaSemana = 7

switch (diaSemana){
    case 1:
        dia = "domingo"
        break
    case 2:
        dia = "segunda"
        break
    case 3:
        dia = "terça"
        break
    case 4:
        dia = "quarta"
        break
    case 5:
        dia = "quinta"
        break
    case 6:
        dia = "sexta"
        break
    case 7:
        dia = "sábado"
        break
    default:
        dia = "Inválido"
}
console.log(dia)