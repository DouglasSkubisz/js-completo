//  LAÇO WHILE

let numero = 5
let contador = 1

while (contador <= 10){
    console.log(`${numero} x ${contador} = ${(numero * contador)}`)
    contador++
}
// LAÇO DO... WHILE

let numeroA = 7
let contadorA = 1

do{
    console.log(`${numeroA} x ${contadorA} = ${(numeroA * contadorA)}`)
    contadorA++
}while(contadorA<=10)

//  LAÇO FOR

let numeroB= 13

for(let contadorB = 1; contadorB <= 10; contadorB++){
    console.log(`${numeroB} x ${contadorB} = ${(numeroB * contadorB)}`)
}